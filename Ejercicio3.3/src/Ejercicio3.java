/*
Diseñar una aplicación  que muestre las tablas de multiplicar del uno al diez
 */

public class Ejercicio3 {

    public static void main(String[] args) {

        for(int i = 1; i < 11; i++){

            for(int j = 0; j < 11; j++){

                System.out.println(  i + " * " + j + " = " + (i * j));

            }

        }


    }

}
